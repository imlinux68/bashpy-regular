#!/bin/python

def calculate():
    firstNum = int(input("Enter first number: "))
    secondNum = int(input("Enter second number: "))
    print("Additional of two numbers is: " + str(firstNum+secondNum))
    print("Substract of two numbers is: " + str(firstNum-secondNum))
    print("Division of two numbers is: " + str(firstNum/secondNum))
    print("Multiply of two numbers is: " + str(firstNum*secondNum))
    print("Module of two numbers is: " + str(firstNum%secondNum))

calculate()